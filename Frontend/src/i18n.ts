// Helper function to replace parameters in translation strings
const replaceParams = (text: string, params: Record<string, string | number> = {}): string => {
  return Object.entries(params).reduce(
    (result, [key, value]) => result.replace(new RegExp(`{{${key}}}`, 'g'), String(value)),
    text
  );
};

// Get language from localStorage or browser settings
export const getInitialLanguage = (): Language => {
  // Check localStorage first
  const savedLang = localStorage.getItem('preferredLanguage') as Language | null;
  if (savedLang && Object.keys(translations).includes(savedLang)) {
    return savedLang;
  }

  // Then check browser language
  const browserLang = navigator.language.split('-')[0] as Language;
  if (browserLang && Object.keys(translations).includes(browserLang)) {
    return browserLang;
  }

  // Default to English
  return 'en';
};

// Current language state (reactive)
let currentLanguage: Language = getInitialLanguage();

// Translation function - uses current language state
export const t = (key: string, params: Record<string, string | number> = {}, lang?: Language): string => {
  const langToUse = lang || currentLanguage;
  const translation = translations[langToUse]?.[key] || translations.en[key] || key;
  return replaceParams(translation, params);
};

// Change language function - updates state without reload
export const changeLanguage = (lang: Language): void => {
  currentLanguage = lang;
  localStorage.setItem('preferredLanguage', lang);
  // Dispatch event for components to react
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
};

// Get current language
export const getCurrentLanguage = (): Language => {
  return currentLanguage;
};

// Set current language (for initialization)
export const setCurrentLanguage = (lang: Language): void => {
  currentLanguage = lang;
};

// Export the translations for use in components if needed
export { translations };
